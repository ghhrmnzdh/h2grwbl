let activeLanguage = 'en'

export function normalizeLanguage(lang) {
  return lang === 'fa' ? 'fa' : 'en'
}

export function getInitialLanguage() {
  try {
    const stored = localStorage.getItem(STORAGE_KEY)
    if (stored) return normalizeLanguage(stored)
  } catch {}

  const param = new URLSearchParams(location.search).get('lang')
  if (param) return normalizeLanguage(param)

  return normalizeLanguage(document.documentElement.lang)
}

export function currentLanguage() {
  return activeLanguage
}

export function currentCopy() {
  return copy[activeLanguage]
}

export function isPersian(lang = activeLanguage) {
  return normalizeLanguage(lang) === 'fa'
}

export function formatNumber(value, lang = activeLanguage, options) {
  return new Intl.NumberFormat(copy[normalizeLanguage(lang)].locale, options).format(value)
}

export function formatMoney(value, lang = activeLanguage) {
  const amount = formatNumber(value, lang)
  return isPersian(lang) ? `$${amount}` : `$${amount}`
}

export function applyLanguage(lang, { persist = true } = {}) {
  const next = normalizeLanguage(lang)
  activeLanguage = next
  const c = copy[next]

  document.documentElement.lang = c.lang
  document.documentElement.dir = c.dir
  document.documentElement.dataset.lang = next
  document.body && (document.body.dataset.lang = next)
  document.title = c.meta.title

  setMeta('description', c.meta.description)
  setMeta('og:title', c.meta.ogTitle, 'property')
  setMeta('og:description', c.meta.ogDescription, 'property')

  bindings.forEach(([selector, key, attr]) => {
    document.querySelectorAll(selector).forEach((el) => {
      const value = c.text[key]
      if (value == null) return
      if (attr) el.setAttribute(attr, value)
      else el.innerHTML = value
    })
  })

  updateLanguageToggle(c)
  updateLanguagePress(c)

  if (persist) {
    try { localStorage.setItem(STORAGE_KEY, next) } catch {}
  }
}

function setMeta(name, content, attr = 'name') {
  const el = document.querySelector(`meta[${attr}="${name}"]`)
  if (el) el.setAttribute('content', content)
}

function updateLanguageToggle(c) {
  const button = document.getElementById('languageToggle')
  if (!button) return
  button.setAttribute('aria-label', c.ui.switchTo)
  button.setAttribute('aria-pressed', String(activeLanguage === 'fa'))
  const text = button.querySelector('.language-toggle__text')
  if (text) text.textContent = c.ui.toggleText
  const mark = button.querySelector('.language-toggle__mark')
  if (mark) mark.textContent = activeLanguage === 'fa' ? 'EN' : 'FA'
}

function updateLanguagePress(c) {
  const mark = document.querySelector('.language-press__mark')
  if (mark) mark.innerHTML = `<span class="reg"><span class="reg-ring"></span></span> ${c.ui.pressMark}`
}

export function principle(n) {
  return currentCopy().principles.find((p) => p.n === n)
}

export function getVisualCopy() {
  return currentCopy().visual
}

export function getEquationCopy() {
  return currentCopy().equation
}

export function alternateLanguage(lang = activeLanguage) {
  return normalizeLanguage(lang) === 'fa' ? 'en' : 'fa'
}
